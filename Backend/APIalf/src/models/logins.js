import mongoose from 'mongoose';
import bcrypt from 'bcrypt';

const loginsSchema = mongoose.Schema({
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

// Middleware para encriptar la contrase√±a antes de guardar
loginsSchema.pre('save', async function(next) {
  if (this.isModified('password')) {
    this.password = await bcrypt.hash(this.password, 10);
  }
  next();
});

export default mongoose.model('Logins', loginsSchema);

